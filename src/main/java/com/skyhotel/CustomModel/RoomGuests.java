package com.skyhotel.CustomModel;

import java.util.List;

public class RoomGuests {
	
	private String AdultNo;
	private String ChildNo;
	private List<Integer> ChildAge;
	
	
	public String getAdultNo() {
		return AdultNo;
	}
	public void setAdultNo(String adultNo) {
		AdultNo = adultNo;
	}
	public String getChildNo() {
		return ChildNo;
	}
	public void setChildNo(String childNo) {
		ChildNo = childNo;
	}
	public List<Integer> getChildAge() {
		return ChildAge;
	}
	public void setChildAge(List<Integer> childAge) {
		ChildAge = childAge;
	}
	
	
	

}

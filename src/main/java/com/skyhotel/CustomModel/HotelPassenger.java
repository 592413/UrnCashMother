package com.skyhotel.CustomModel;

public class HotelPassenger {
	private String RoomCode;
	private String Title;
	private String FirstName;
	private String LastName;
	private String Phoneno;
	private String Email;
	private int PaxType;
	private boolean LeadPassenger;
	private String Age;
	public String getRoomCode() {
		return RoomCode;
	}
	public void setRoomCode(String roomCode) {
		RoomCode = roomCode;
	}
	public String getTitle() {
		return Title;
	}
	public void setTitle(String title) {
		Title = title;
	}
	public String getFirstName() {
		return FirstName;
	}
	public void setFirstName(String firstName) {
		FirstName = firstName;
	}
	public String getLastName() {
		return LastName;
	}
	public void setLastName(String lastName) {
		LastName = lastName;
	}
	public String getPhoneno() {
		return Phoneno;
	}
	public void setPhoneno(String phoneno) {
		Phoneno = phoneno;
	}
	public String getEmail() {
		return Email;
	}
	public void setEmail(String email) {
		Email = email;
	}
	public int getPaxType() {
		return PaxType;
	}
	public void setPaxType(int paxType) {
		PaxType = paxType;
	}
	public boolean isLeadPassenger() {
		return LeadPassenger;
	}
	public void setLeadPassenger(boolean leadPassenger) {
		LeadPassenger = leadPassenger;
	}
	public String getAge() {
		return Age;
	}
	public void setAge(String age) {
		Age = age;
	}
	
	
	
	
}

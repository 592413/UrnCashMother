package com.skyflight.searchCustomModel;

public class CalendarFareResults {
	private String AirlineCode;
	private String AirlineName;
	private String DepartureDate;
	private boolean IsLowestFareOfMonth;
	private String Fare;
	private String BaseFare;
	private String Tax;
	private String OtherCharges;
	private String FuelSurcharge;
	public String getAirlineCode() {
		return AirlineCode;
	}
	public void setAirlineCode(String airlineCode) {
		AirlineCode = airlineCode;
	}
	public String getAirlineName() {
		return AirlineName;
	}
	public void setAirlineName(String airlineName) {
		AirlineName = airlineName;
	}
	public String getDepartureDate() {
		return DepartureDate;
	}
	public void setDepartureDate(String departureDate) {
		DepartureDate = departureDate;
	}
	
	
	public boolean isIsLowestFareOfMonth() {
		return IsLowestFareOfMonth;
	}
	public void setIsLowestFareOfMonth(boolean isLowestFareOfMonth) {
		IsLowestFareOfMonth = isLowestFareOfMonth;
	}
	public String getFare() {
		return Fare;
	}
	public void setFare(String fare) {
		Fare = fare;
	}
	public String getBaseFare() {
		return BaseFare;
	}
	public void setBaseFare(String baseFare) {
		BaseFare = baseFare;
	}
	public String getTax() {
		return Tax;
	}
	public void setTax(String tax) {
		Tax = tax;
	}
	public String getOtherCharges() {
		return OtherCharges;
	}
	public void setOtherCharges(String otherCharges) {
		OtherCharges = otherCharges;
	}
	public String getFuelSurcharge() {
		return FuelSurcharge;
	}
	public void setFuelSurcharge(String fuelSurcharge) {
		FuelSurcharge = fuelSurcharge;
	}
	
	
	

}

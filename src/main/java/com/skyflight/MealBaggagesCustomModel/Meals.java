package com.skyflight.MealBaggagesCustomModel;

public class Meals {
	private String WayType;
	private String Code;
	private String Description;
	private int Amount;
	private String Origin;
	private String Destination;
	private String FlightNumber;
	private String AirlineDescription;
	public String getWayType() {
		return WayType;
	}
	public void setWayType(String wayType) {
		WayType = wayType;
	}
	public String getCode() {
		return Code;
	}
	public void setCode(String code) {
		Code = code;
	}
	public String getDescription() {
		return Description;
	}
	public void setDescription(String description) {
		Description = description;
	}
	public int getAmount() {
		return Amount;
	}
	public void setAmount(int amount) {
		Amount = amount;
	}
	public String getOrigin() {
		return Origin;
	}
	public void setOrigin(String origin) {
		Origin = origin;
	}
	public String getDestination() {
		return Destination;
	}
	public void setDestination(String destination) {
		Destination = destination;
	}
	public String getFlightNumber() {
		return FlightNumber;
	}
	public void setFlightNumber(String flightNumber) {
		FlightNumber = flightNumber;
	}
	public String getAirlineDescription() {
		return AirlineDescription;
	}
	public void setAirlineDescription(String airlineDescription) {
		AirlineDescription = airlineDescription;
	}
	
	

}
